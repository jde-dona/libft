#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 48 && str[i] <= 57)
		{
			n *= 10;
			n += (str[i] - 48);
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (n);
}
