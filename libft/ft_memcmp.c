#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const char unsigned	*s3;
	const char unsigned	*s4;
	size_t				i;

	i = 0;
	s3 = (const char unsigned *) s1;
	s4 = (const char unsigned *) s2;
	while (i < n)
	{
		if (s3[i] != s4[i])
			return (s3[i] - s4[i]);
		i++;
	}
	return (0);
}
