#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*d;

	i = n - 1;
	d = (char *) s;
	while (i >= 0)
	{
		d[i] = c;
		i--;
	}
	return (d);
}
