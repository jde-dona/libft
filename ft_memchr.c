void	*ft_memchr(const void *str, int c, int n)
{
	char *str1 = (char *)str;
	int i = 0;
	while (i < n)
	{
		if (str1[i] == c)
		{
			return &str1[i];
		}
		i++;
	}
	return 0;
}
